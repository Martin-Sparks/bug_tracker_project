import FilePondPluginImagePreview from 'filepond-plugin-image-preview';
 
// Create component
const FilePond = vueFilePond(FilePondPluginFileValidateType, FilePondPluginImagePreview);
 
export default {
    name: 'filepondtest',
    data: function() {
        return { myFiles: null };
    },
    methods: {
